    public void barcode()
    {
        try {
            String pcode = txtpcode.getText();
            pst = (PreparedStatement) con.prepareStatement("select * from product where barcode = ?");
            pst.setString(1, pcode);
            rs = pst.executeQuery();
            
           if(rs.next() == false)
           {
               JOptionPane.showMessageDialog(this, "BarCode Not Found");
               txtpcode.setText("");   
           }
           else    
           {
               String pname = rs.getString("pname");
               String price = rs.getString("rprice");
               
                 txtpname.setText(pname.trim());
                 txtprice.setText(price.trim());
                 txtqty.requestFocus();   
           }
        } catch (SQLException ex) {
            Logger.getLogger(purchase.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
    
     public void Sales()
    {
        
        try {
            String pcode = txtpcode.getText();
            pst = (PreparedStatement) con.prepareStatement("select * from product where barcode = ?");
            pst.setString(1, pcode);
            rs = pst.executeQuery();
            
            while(rs.next())
            {
                int currentqty;
                currentqty = rs.getInt("qty");
             int price = Integer.parseInt(txtprice.getText());
            int qty = Integer.parseInt(txtqty.getText());
            int tot = price * qty;
            
            
            if(qty >= currentqty)
            {
                JOptionPane.showMessageDialog(this, "Qty Not Enough!!!!!!!!!!");
            }
            
            else
            {
                 df = (DefaultTableModel)jTable1.getModel();
                 df.addRow(new Object[]
                    
            {
                txtpcode.getText(),
                txtpname.getText(),
                txtprice.getText(),
                txtqty.getText(),
                tot 
            });
            } 
            }
            int sum = 0;
            for(int i = 0; i<jTable1.getRowCount(); i++)
            {
                sum = sum + Integer.parseInt(jTable1.getValueAt(i, 4).toString());   
            }
            txttcost.setText(String.valueOf(sum));
            txtpcode.setText("");
            txtpname.setText("");
            txtprice.setText("");
            txtqty.setText("");
        } catch (SQLException ex) {
            Logger.getLogger(sales.class.getName()).log(Level.SEVERE, null, ex);
        }      
    }
    
    public void add()
    {
        try {
            DateTimeFormatter dt = DateTimeFormatter.ofPattern("yyyy/MM/dd");
            LocalDateTime now = LocalDateTime.now();
            String date = dt.format(now);
           
            String subtotal = txttcost.getText();
            String pay = txtpay.getText();
            String bal = txtbal.getText();
            int lastid = 0;
            
            
            String query1 = "insert into sales(date,subtotal,pay,bal)values(?,?,?,?)" ;
            pst = (PreparedStatement) con.prepareStatement(query1,Statement.RETURN_GENERATED_KEYS);
            
            pst.setString(1, date);
            pst.setString(2, subtotal);
            pst.setString(3, pay);
            pst.setString(4, bal);
            pst.executeUpdate();
            rs = pst.getGeneratedKeys();
            
            if(rs.next())
            {
                
                lastid = rs.getInt(1);
            }
            
            
            String query2 = "insert into sales_product(sales_id,pid,price,qty,total)values(?,?,?,?,?) ";
            pst1 = (PreparedStatement) con.prepareStatement(query2);
            
          
              String productid;
              String price;
              String qty;
             int total = 0;
             
             
             
             for(int i=0; i<jTable1.getRowCount(); i++)
             {
                 productid = (String)jTable1.getValueAt(i, 0);
                  price = (String)jTable1.getValueAt(i, 2);
                  qty = (String)jTable1.getValueAt(i, 3);
                  total = (int)jTable1.getValueAt(i, 4);
                  
                  
                  pst1.setInt(1, lastid);
                  pst1.setString(2, productid);
                   pst1.setString(3, price);
                   pst1.setString(4, qty);
                   pst1.setInt(5, total);
                   pst1.executeUpdate();        
             }
             
             
             String query3 = "update product set qty = qty- ?   where barcode = ?";
             pst2 = (PreparedStatement) con.prepareStatement(query3);
             
               
             for(int i=0; i<jTable1.getRowCount(); i++)
             {
                 productid = (String)jTable1.getValueAt(i, 0);
                 qty = (String)jTable1.getValueAt(i, 3);
                 
                 pst2.setString(1, qty);
                 pst2.setString(2, productid);
                 pst2.executeUpdate();

             }
             
             
             JOptionPane.showMessageDialog(this, "Sales Completed");
             
             
             HashMap p = new HashMap();
             p.put("invo", lastid);
             
            
        } catch (SQLException ex) {
            Logger.getLogger(purchase.class.getName()).log(Level.SEVERE, null, ex);
        }
     
     
    }
    
    
    
    
    
    
    
    
    
    
    
    
    
    

